    import csv

    for name, items in (("tem4", TEM4), ("tem8", TEM8)):
        csv_path = OUT_DIR / f"image_prompts_{name}.csv"
        with csv_path.open("w", encoding="utf-8-sig", newline="") as f:
            w = csv.writer(f)
            w.writerow(["西语", "义项", "中文释义", "图片文件名", "配图提示词"])
            for lemma, sense, _cat, _level, zh, _es, _zh_ex, scene in items:
                w.writerow([lemma, sense, zh, image_name(lemma, sense), prompt(scene)])
        print(f"Wrote {csv_path}")


