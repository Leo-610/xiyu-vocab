def save_book(path: Path, items: list[tuple] | None, sheet: str | None, combined: bool) -> None:
    wb = Workbook()
    # remove default
    default = wb.active
    wb.remove(default)
    if combined:
        write_guide(wb)
        write_sheet(wb, "专四", TEM4)
        write_sheet(wb, "专八", TEM8)
    else:
        assert items is not None and sheet is not None
        write_sheet(wb, sheet, items)
    path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(path)
    print(f"Wrote {path} ({path.stat().st_size} bytes)")


