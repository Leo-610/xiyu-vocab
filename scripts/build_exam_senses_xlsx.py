def image_name(lemma: str, sense: int) -> str:
    slug = (
        lemma.lower()
        .replace("á", "a")
        .replace("é", "e")
        .replace("í", "i")
        .replace("ó", "o")
        .replace("ú", "u")
        .replace("ü", "u")
        .replace("ñ", "n")
        .replace(" ", "_")
    )
    slug = "".join(ch if ch.isalnum() or ch == "_" else "_" for ch in slug)
    return f"images/{slug}_{sense}.png"


def rows_from(items: list[tuple]) -> list[list]:
    out = []
    for lemma, sense, cat, level, zh, es, zh_ex, scene in items:
        out.append(
            [
                lemma,
                sense,
                cat,
                level,
                zh,
                es,
                zh_ex,
                image_name(lemma, sense),
                prompt(scene),
            ]
        )
    return out


def style_header(ws) -> None:
    fill = PatternFill("solid", fgColor="1F4E79")
    font = Font(color="FFFFFF", bold=True)
    for col, h in enumerate(HEADERS, 1):
        cell = ws.cell(1, col, h)
        cell.fill = fill
        cell.font = font
        cell.alignment = Alignment(wrap_text=True, vertical="center")
    ws.freeze_panes = "A2"
    widths = [22, 8, 12, 8, 22, 56, 40, 28, 70]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


def write_sheet(wb: Workbook, title: str, items: list[tuple]) -> None:
    ws = wb.create_sheet(title)
    style_header(ws)
    for r, row in enumerate(rows_from(items), 2):
        for c, val in enumerate(row, 1):
            cell = ws.cell(r, c, val)
            cell.alignment = Alignment(wrap_text=True, vertical="top")
    ws.row_dimensions[1].height = 22


